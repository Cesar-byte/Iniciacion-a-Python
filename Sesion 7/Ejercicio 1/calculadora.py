def sumar (a,b):
    '''
     Devuelve el resultado de sumar a y b (a+b)
    '''    
    return a+b

def restar (a,b):
    '''
     Devuelve el resultado de restar a y b (a-b)
    '''    
    return a - b

def multiplicar (a,b):
    '''
     Devuelve el resultado de multiplicar a y b (a*b)
    '''    
    return a*b

def dividir (a,b):
    '''
     Devuelve el resultado de dividir a y b (a/b)
    '''    
    return a/b